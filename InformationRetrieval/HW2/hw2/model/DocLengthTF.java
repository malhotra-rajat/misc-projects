package hw2.model;

public class DocLengthTF {
	int doc_length;
	int tf;
	
	
	public DocLengthTF(int doc_length, int tf) {
		super();
		this.doc_length = doc_length;
		this.tf = tf;
	}
	
	public int getDoc_length() {
		return doc_length;
	}
	public void setDoc_length(int doc_length) {
		this.doc_length = doc_length;
	}
	public int getTf() {
		return tf;
	}
	public void setTf(int tf) {
		this.tf = tf;
	}
	
	

	
	
}
